    {
        public BasicPage1()
        {
            InitializeComponent();

            ipInfo.Text = "Адрес для удаленногоподключения - " + GetLocalIPAddress();

        }

        public static string GetLocalIPAddress()
        {
            var host = Dns.GetHostEntry(Dns.GetHostName());
            foreach (var ip in host.AddressList)
            {
                if (ip.AddressFamily == AddressFamily.InterNetwork)
                {
                    return ip.ToString();
                }
            }
            throw new Exception("Не определен! Проверьте настройки сети.");
        }
    }
